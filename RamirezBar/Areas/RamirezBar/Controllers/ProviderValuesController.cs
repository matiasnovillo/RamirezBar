        [HttpPost("~/api/RamirezBar/Provider/1/InsertOrUpdateAsync")]
        public async Task<IActionResult> InsertOrUpdateAsync()
        {
            try
            {
                //Get UserId from Session
                int UserId = HttpContext.Session.GetInt32("UserId") ?? 0;

                if(UserId == 0)
                {
                    return StatusCode(401, "User not found in session");
                }
                
                #region Pass data from client to server
                //ProviderId
                int ProviderId = Convert.ToInt32(HttpContext.Request.Form["ramirezbar-provider-providerid-input"]);
                
                string Name = HttpContext.Request.Form["ramirezbar-provider-name-input"];
                string Address = HttpContext.Request.Form["ramirezbar-provider-address-input"];
                string Phone1 = HttpContext.Request.Form["ramirezbar-provider-phone1-input"];
                string Phone2 = HttpContext.Request.Form["ramirezbar-provider-phone2-input"];
                
                #endregion

                int NewEnteredId = 0;
                int RowsAffected = 0;

                if (ProviderId == 0)
                {
                    //Insert
                    ProviderModel ProviderModel = new ProviderModel()
                    {
                        Active = true,
                        UserCreationId = UserId,
                        UserLastModificationId = UserId,
                        DateTimeCreation = DateTime.Now,
                        DateTimeLastModification = DateTime.Now,
                        Name = Name,
                        Address = Address,
                        Phone1 = Phone1,
                        Phone2 = Phone2,
                        
                    };
                    
                    NewEnteredId = _IProvider.Insert(ProviderModel);
                }
                else
                {
                    //Update
                    ProviderModel ProviderModel = new ProviderModel(ProviderId);
                    
                    ProviderModel.UserLastModificationId = UserId;
                    ProviderModel.DateTimeLastModification = DateTime.Now;
                    ProviderModel.Name = Name;
                    ProviderModel.Address = Address;
                    ProviderModel.Phone1 = Phone1;
                    ProviderModel.Phone2 = Phone2;
                                       

                    RowsAffected = _IProvider.UpdateByProviderId(ProviderModel);
                }
                

                //Look for sent files
                if (HttpContext.Request.Form.Files.Count != 0)
                {
                    int i = 0; //Used to iterate in HttpContext.Request.Form.Files
                    foreach (var File in Request.Form.Files)
                    {
                        if (File.Length > 0)
                        {
                            var FileName = HttpContext.Request.Form.Files[i].FileName;
                            var FilePath = $@"{_WebHostEnvironment.WebRootPath}/Uploads/RamirezBar/Provider/";

                            using (var FileStream = new FileStream($@"{FilePath}{FileName}", FileMode.Create))
                            {
                                
                                await File.CopyToAsync(FileStream); // Read file to stream
                                byte[] array = new byte[FileStream.Length]; // Stream to byte array
                                FileStream.Seek(0, SeekOrigin.Begin);
                                FileStream.Read(array, 0, array.Length);
                            }

                            i += 1;
                        }
                    }
                }

                if (ProviderId == 0)
                {
                    return StatusCode(200, NewEnteredId); 
                }
                else
                {
                    return StatusCode(200, RowsAffected);
                }
            }
            catch (Exception ex) 
            { 
                DateTime Now = DateTime.Now;
                FailureModel FailureModel = new FailureModel()
                {
                    HTTPCode = 500,
                    Message = ex.Message,
                    EmergencyLevel = 1,
                    StackTrace = ex.StackTrace ?? "",
                    Source = ex.Source ?? "",
                    Comment = "",
                    Active = true,
                    UserCreationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    UserLastModificationId = HttpContext.Session.GetInt32("UserId") ?? 1,
                    DateTimeCreation = Now,
                    DateTimeLastModification = Now
                };
                FailureModel.Insert();
                return StatusCode(500, ex.Message);
            }
        }

